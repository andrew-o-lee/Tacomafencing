<h3>Epee</h3>

<article>
	<p>Touches in epee are also only scored with the tip of the blade; however, unlike foil, the entire body is valid target area. In contrast with foil and saber, there are no right of way rules for epee. This is the traditional dueling weapon.</p>
	<p>Coach Dana Sanford is our Head Epee Coach and teaches our Beginning Epee class.</p>
	<p>Open epee times (usually Thursday evenings) and private lessons are available. </p>
</article>

<hr>

<h3>Saber</h3>

<article>
	<p>In saber, both the tip and the edge of the blade can be used to score touches. Everything above the waist is target except for the back of the head and the hands. Saber is the fastest of the three disciplines. </p>

	<p>Coach Joe Jasper teaches an Beginning Fencing (all three weapons-foil, epee, and saber) class on Saturday mornings. Coach Joe will also provide private saber lessons.</p>
</article>


