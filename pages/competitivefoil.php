<h2>Competitive Foil</h2>

<article>

	<p>The Competitive Foil class builds on the fencing skills developed in the Beginning and Intermediate Class. The lessons are more robust and fencers are expected to have a solid foundational framework for competitive-level fencing. Fencers in the Competitive Foil class typically compete regularly in local fencing tournaments and/or make fencing a significant part of their physical conditioning routine. Private lessons are encouraged at this level. </p>

	<p>This is a year-round program and students are expected to participate for the full 12 months (season starting in August). Students are encouraged to compete in local events. They are also encouraged to compete in the regional events (RYC/RJCC/ROC) and may attend Summer Nationals/July Challenge.</p>

</article>

<hr>

<h2>Elite Foil</h2>

<article>
	<p>The Elite Program is designed to provide a high-quality, high-level, training program to develop highly competitive fencers to compete at local, regional, and national fencing tournaments.</p>

	<p>The Elite Program focuses on developing and improving fencing skills for highly motivated competitive fencers (e.g., group instruction, paired drills, situational bouting, one-on-one instruction, conditioning, and high level open fencing).</p>

	<p>Participation is limited to highly skilled fencers who will compete at regional and national-level fencing tournaments. Participation requires consistent, committed, engaged participation and is by invitation only.</p>

	<p>This is a year-round program and students are expected to participate for the full 12 months (season starting in August). Students are encouraged to compete in local and regional events (RYC/RJCC/ROC) and may attend Summer Nationals/July Challenge. Some in the elite program have even competed internationally.</p>

</article>

<hr>

<h2>Elite Epee</h2>

<article>
	<p>Designed for advanced epee fencers wanting to compete at regional, national, and international tournaments. Led by Coach Jisun Kim. The program will focus on improving fencing techniques and skills, learning and mastering fencing strategies, and physical conditioning to improve speed, strength, and stamina.  </p>


</article>


