<h3>Foil</h3>

<article>
	<p>In foil, touches are scored only with the tip of the blade. Only the torso is valid target area. Foil and saber have "right of way" rules which determine who receives a touch.</p> 
	<p>New and less experienced fencers will start with the Beginning Foil class and, depending on the level of their participation and goals, move on to Intermediate, and then the Competitive Class. As their skills improve, those interested in high level training and significant competition may enroll in the Elite Program.</p>
</article>

