<h3>Epee</h3>

<article>
	<p>Like foil, touches in epee are only scored with the tip of the blade, but with epee the entire body is valid target area. Unlike foil and saber, you do not have to worry about right of way rules for epee. Epees are heavier than the other two weapons.</p>
	<p>Coach Dana Sanford is our Head Epee Coach. We have a new Introduction to Epee class.</p>
